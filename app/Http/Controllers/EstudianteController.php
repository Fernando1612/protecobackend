     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $user=User::findOrFail($id);
        $user->origin=$request->origin;


        $probatorio = $request->probatorio;
        $extension = $probatorio->getClientOriginalExtension(); // method returns extension.
        $name= "Probatorio_".auth()->user()->fname.auth()->user()->fname.".".$extension;
        $ruta = public_path().'/probatorios';
        $probatorio->move($ruta, $name);
        $user->probatorio=$name;

        $user->update();

        return redirect()->back()->with('probatorio','Tu informaci√≥n ha sido actualizada');
    }

